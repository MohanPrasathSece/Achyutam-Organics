-- Public access policies
-- Categories - Read access for all
CREATE POLICY "Categories are viewable by everyone" ON categories
    FOR SELECT USING (true);

-- Products - Read access for all
CREATE POLICY "Products are viewable by everyone" ON products
    FOR SELECT USING (status = 'active');

-- Gift Options - Read access for all
CREATE POLICY "Gift options are viewable by everyone" ON gift_options
    FOR SELECT USING (available = true);

-- Orders - Users can only see their own orders
CREATE POLICY "Users can view their own orders" ON orders
    FOR SELECT USING (auth.email() = email);

-- Order status history - Related to order access
CREATE POLICY "Order status history is viewable based on order access" ON order_status_history
    FOR SELECT USING (
        EXISTS (
            SELECT 1 FROM orders WHERE orders.id = order_status_history.order_id
            AND auth.email() = orders.email
        )
    );

-- Functions for automatic timestamps
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Triggers for updated_at
CREATE TRIGGER update_categories_updated_at BEFORE UPDATE ON categories
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_products_updated_at BEFORE UPDATE ON products
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_gift_options_updated_at BEFORE UPDATE ON gift_options
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_orders_updated_at BEFORE UPDATE ON orders
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

-- Function to create order status history
CREATE OR REPLACE FUNCTION create_order_status_history()
RETURNS TRIGGER AS $$
BEGIN
    INSERT INTO order_status_history (order_id, status, notes, created_by)
    VALUES (NEW.id, NEW.status, 'Order created', 'system');
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE TRIGGER create_order_history_trigger AFTER INSERT ON orders
    FOR EACH ROW EXECUTE FUNCTION create_order_status_history();

-- Function to update inventory on order creation
CREATE OR REPLACE FUNCTION update_inventory_on_order()
RETURNS TRIGGER AS $$
DECLARE
    item JSONB;
    product_uuid UUID;
    quantity INTEGER;
BEGIN
    -- Loop through order items
    FOR item IN SELECT * FROM jsonb_array_elements(NEW.items)
    LOOP
        product_uuid := (item->>'id')::UUID;
        quantity := (item->>'quantity')::INTEGER;
        
        -- Update product stock
        UPDATE products 
        SET stock_quantity = stock_quantity - quantity
        WHERE id = product_uuid;
        
        -- Create inventory movement record
        INSERT INTO inventory_movements (product_id, movement_type, quantity, reference_id, notes)
        VALUES (product_uuid, 'sale', -quantity, NEW.id, 'Order #' || NEW.order_number);
    END LOOP;
    
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE TRIGGER update_inventory_trigger AFTER INSERT ON orders
    FOR EACH ROW WHEN (NEW.status = 'confirmed')
    EXECUTE FUNCTION update_inventory_on_order();

